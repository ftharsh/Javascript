let a = "33";
let b = 44;

// Console log the sum (concatenation)
console.log(a + b); // Output: "3344"

// Console log the data type of the sum
console.log(typeof (a + b)); // Output: "string"

// Console log the difference
console.log(a - b); // Output: -11

// Console log the data type of the difference
console.log(typeof (a - b)); // Output: "number"
